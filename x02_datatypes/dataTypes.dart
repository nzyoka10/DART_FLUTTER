void main() {
  // Example of different data types in Dart

  // String
  String name = 'John Doe';

  // Integer
  int age = 25;

  // Double
  double height = 6.0;

  // Boolean
  bool isStudent = true;

  // List
  List<String> hobbies = ['Reading', 'Gaming', 'Traveling'];

  // Map
  Map<String, dynamic> personInfo = {
    'name': 'John Doe',
    'age': 25,
    'height': 6.0,
    'isStudent': true,
    'hobbies': ['Reading', 'Gaming', 'Traveling']
  };

  // Printing out the data
  print('Name: $name');
  print('Age: $age');
  print('Height: $height');
  print('Is Student: $isStudent');
  print('Hobbies: $hobbies');
  print('Person Info: $personInfo');
}
