// Task 5: Create a program that uses an if-else statement to check if a number is even or odd and output the result.

void main() {
  // Example number
  int number = 17;

  // Checking if the number is even or odd
  if (number % 2 == 0) {
    print('$number is even');
  } else {
    print('$number is odd');
  }
}
